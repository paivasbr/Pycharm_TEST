# EXEMPLO BOM

PI = 3.14 # Definindo uma constante para o valor de pi


# Função para calcular a área de um circulo

def calcular_area_circulo(raio):
    area = PI * raio ** 2
    return area

# Chamada da função com um raio definido em forma significativa
raio_do_circulo = 5
area_do_circulo = calcular_area_circulo(raio_do_circulo)
print("A área do circulo é:", area_do_circulo)

